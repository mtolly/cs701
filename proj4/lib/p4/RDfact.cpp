#include "RDfact.h"

//**********************************************************************
// constructor
//**********************************************************************
RDfact::RDfact(unsigned reg, MachineInstr *inst) {
  myReg = reg;
  myInstr = inst;
}

//**********************************************************************
// getReg
//**********************************************************************
unsigned RDfact::getReg() {
  return myReg;
}

//**********************************************************************
// getInstr
//**********************************************************************
MachineInstr *RDfact::getInstr() {
  return myInstr;
}
